# TRAFFIC LIGHT

light = input("light : ")
if(light == "red"):          # IF THIS CONDITION HOLDS TRUE THEN STOP WILL BE PRINTED OTHERWISE GOES TO NEXT CONDITION
    print("Stop")

elif(light == "yellow"):     # IF THIS CONDITION HOLDS TRUE THEN STOP WILL BE PRINTED OTHERWISE GOES TO NEXT CONDITION
    print("Look")

elif(light == "green"):      # IF THIS CONDITION HOLDS TRUE THEN STOP WILL BE PRINTED OTHERWISE GOES TO NEXT CONDITION
    print("Go")

else:                        # IF ANY OF THE PREVIOUS CONDITION DOES NOT HOLDS TRUE THEN THIS WILL BE PRINTED 
    print("traffic light is not working properly")